package abs_test

import (
	"testing"

	keepertest "github.com/be-heroes/doxchain/testutil/keeper"
	"github.com/be-heroes/doxchain/testutil/nullify"
	"github.com/be-heroes/doxchain/x/abs"
	"github.com/be-heroes/doxchain/x/abs/types"
	"github.com/stretchr/testify/require"
)

func TestGenesis(t *testing.T) {
	genesisState := types.GenesisState{
		Params: types.DefaultParams(),

		PartitionedPoolsList: []types.PartitionedPools{
			{
				Index: "0",
			},
			{
				Index: "1",
			},
		},
		// this line is used by starport scaffolding # genesis/test/state
	}

	k, ctx := keepertest.AbsKeeper(t)
	abs.InitGenesis(ctx, *k, genesisState)
	got := abs.ExportGenesis(ctx, *k)
	require.NotNil(t, got)

	nullify.Fill(&genesisState)
	nullify.Fill(got)

	require.ElementsMatch(t, genesisState.PartitionedPoolsList, got.PartitionedPoolsList)
	// this line is used by starport scaffolding # genesis/test/assert
}
